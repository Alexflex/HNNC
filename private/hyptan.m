function y = hyptan(x)
% Функция активации вида масштабированный гип. тангенс
% особенностью является то, что hyptan(+-1) = +-1
%
% y = hyptan(x)
%
% Arguments
% x - индуцированное локальное поле нейрона
%
% y - выход нейрона 
%
% Example
% y = hyptan(x);
%
% See also
% tanh
% Саймон Хайкин Нейронные сети: полный курс = 
% Neural Networks: A Comprehensive Foundation. — 2-е. — 
% М.: «Вильямс», 2006. — С. 1104. — ISBN 0-13-273350-1
% 
% Revisions
% Author: Vulfin Alex, Date: 17/11/2010
% Supervisor: Vulfin Alex, Date: 17/11/2010
% Author: (Next revision author), Date: (Next revision date)

% масштабирующие коэффициенты
a = 1.7159;
b = 2/3;

y = a*tanh(b*x);

end %of function